// An object in JavaScript is a collection of properties, where each property has a name and a value. Objects are used to store and organize data in a structured way, and they can be used to model real-world objects and their properties.

// You can create an object using the object literal notation, which is a pair of curly braces {} with properties separated by commas, like this:

let myObject = {
  name: "John",
  age: 30,
  isStudent: false,
};

// You can also create an object using the Object constructor, like this:

let myObject2 = new Object();
myObject.name = "John";
myObject.age = 30;
myObject.isStudent = false;
